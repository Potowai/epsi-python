# Liste de noms
noms = ["Alice", "Bob", "Charlie", "David", "Eva"]

# Tri de la liste par ordre alphabétique inverse
noms_tries_inverse = sorted(noms, reverse=True)

# Affichage des noms triés par ordre alphabétique inverse
print("Liste des noms triés par ordre alphabétique inverse :")
for nom in noms_tries_inverse:
    print(nom)
